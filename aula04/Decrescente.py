for n in range(10, 0, -1):
    print(n)

# Explicação do código:

# - Este loop for percorre os números de 10 a 1 (inclusive), decrementando de 1 em 1.

# - A função range(10, 0, -1) gera uma sequência de números de 10 a 1, com passo de -1 (ou seja, decrementando de 1 em 1)

# - Para cada número na sequência, o loop imprime o valor na tela.

