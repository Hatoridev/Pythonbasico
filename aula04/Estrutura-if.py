idade = int(input("Digite a idade da pessoa: "))

if idade > 18:
    print("maior de idade")
else:
    print("menor de idade")

# Explicação do código:

# - Solicitamos ao usuário que digite a idade da pessoa e convertemos a entrada para um número inteiro.

# - Verificamos se a idade é maior que 18.

# - Se a condição for verdadeira, imprime "maior de idade".

# - Se a condição for falsa, imprime "menor de idade".

