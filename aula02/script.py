def conversa():
    print("Lucas: Olá meu amigo, tudo bem contigo?")
    input("Pressione Enter para continuar...")

    print("João: Tudo ótimo, Lucas.")
    input("Pressione Enter para continuar...")

    print("João: Que horas são aí, Lucas?")
    input("Pressione Enter para continuar...")

    print("Lucas: 18:00h")
    input("Pressione Enter para prosseguir...")

conversa()

def ligacao():
    print("Geovana: Alô?")
    input("Pressione Enter para escutar...")

    print("Amanda: Amiga, você já assistiu Pânico 6?")
    input("Pressione Enter para escutar...")

    print("Geovana: Já sim amiga... Vamos assistir juntas?")
    input("Pressione Enter para escutar...")

    print("Amanda: Claro, vamos sim!")

ligacao()

# Explicação do código:

# - Definimos duas funções, conversa() e ligacao(), que simulam diálogos entre pessoas.

# - Dentro de cada função, imprimimos mensagens e solicitamos ao usuário que pressione Enter para continuar.

# - Depois de imprimir os diálogos, chamamos a função conversa() para iniciar o diálogo entre Lucas e João.

# - Finalmente, chamamos a função ligacao() para simular uma ligação entre Geovana e Amanda.

