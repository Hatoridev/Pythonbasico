# - A função message1() imprime "Hello World" na tela.

# - A função message2() retorna a string 'Hello World'.

# - Chamamos a função message1(), que imprime "Hello World".

# - Chamamos a função message2() e armazenamos o resultado retornado em uma variável chamada texto.

