x = 1
while x <= 15:
    print(x)
    x = x + 1

# Explicação do código:

# - Inicializamos a variável x com o valor 1.

# - Entramos em um loop while que continua enquanto x for menor ou igual a 15.

# - Em cada iteração do loop, imprimimos o valor de x.

# - Incrementamos o valor de x em 1 em cada iteração, usando x = x + 1.

# - O loop continua até que x seja maior que 15.

